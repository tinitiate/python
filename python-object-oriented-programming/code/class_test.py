class OOP:

    # Class Variables
    num1 = None
    num2 = None
            
    # Class Functions
    def add1(self, N1, N2):
        return N1 + N2

    def mul1(self, N1, N2):
        return N1 * N2

    def print_class_vars(self):
        print("Class OOPs vars:")
        print("num1",self.num1)
        print("num2",self.num2)
