# Test Variables
A = 1
B = 2
C = 3

# Sum, Difference, product, divide, mod, power
# Add, subtract, multiply 
print(A+B+C) # OUTPUT: 6
print(A-B)   # OUTPUT: -1
print(100/3) # OUTPUT: 33.333333333333336

# remainder, mod
print(100%3) # OUTPUT: 1

# power, exponential operator
print(4**2)  # OUTPUT: 16

# Augmented assign
A += 4   # A=A+4
print(A) # OUTPUT: 5  

B *= 4   # B=B*4
print(B) # OUTPUT: 8

# For strings
S="-hello-"
S *= 3  # S = S*3
print(S) # OUTPUT: -hello--hello--hello-