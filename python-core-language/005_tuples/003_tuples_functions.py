tuple99 = (11, 22, 33, 44, 55, 66)
# COUNT method counts the occurrences of an element in the tuple
print((1,1,2,3).count(1)) # OUTPUT: 2 

# MIN and MAX element of the TUPLE
print(min(tuple99), max(tuple99)) # OUTPUT: 11 66

# LENGTH of tuple
print(len(tuple99))

# SUM of all ELEMENTS
print(sum(tuple99)) 
# OUTPUT: 231